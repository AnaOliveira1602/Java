package Principal.Modelo;

public class Pessoa {
	
	private String nome;
	private String CPF;
	private int peso;
	
	public String getNome() {
		return nome;
	}
	
	public String getCPF() {
		return CPF;
	}
	
	public int getPeso() {
		return peso;
	}
	
	
	
	
	public void setNome(String nome) {
		this.nome = nome;
	}
	
	public void setCPF(String CPF) {
		this.CPF = CPF;
	}
	
	public void setPeso(int peso) {
		this.peso = peso;
	}
	
	
	
	
	
}

package Principal.pessoa;

public class pessoa {
	
	private String nome;
	private int CPF;
	private int peso;
	
	public String getNome() {
		return nome;
	}
	
	public int getCPF() {
		return CPF;
	}
	
	public int getPeso() {
		return peso;
	}
	
	
	
	
	public void setNome(String nome) {
		this.nome = nome;
	}
	
	public void setCPF(int CPF) {
		this.CPF = CPF;
	}
	
	public void setPeso(int peso) {
		this.peso = peso;
	}
	
	
	
	
	
}

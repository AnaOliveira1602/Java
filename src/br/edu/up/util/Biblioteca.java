package br.edu.up.util;

public class Biblioteca {
	public static int somar(int a, int b) {
	return a + b;
}

public static int sub(int a, int b) {
	return a - b;
}

public static int mult(int a, int b) {
	return a * b;
}

public static int divi(int a, int b) {
	return a / b;
}

}
